% случай 5: если предпосылки образуют конъюнкцию
как1(H, Правило :: если Дерево1 и Дерево2 то _) :- 
                как(H, Дерево1), как(H, Дерево2).*/

% вывод правила на экран
отобрази_правило(Правило :: если H1 то H) :-
        write(Правило), write(':'), nl,
        write('если '), write(H1), nl,
        write('то '), write(H), nl.


