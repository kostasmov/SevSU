% объяснение вывода утверждения
объясни(Дерево) :- write( 'объяснить ? [цель/нет]:'), nl, read(H),
                   (H \= нет, !, как(H, Дерево), объясни(Дерево)) ; !.

% поиск следующих решений
возврат :- nl, write('Искать ещё решение [да/нет]?: '), nl, read(нет).
